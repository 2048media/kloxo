
There are two extra options: --chooser-- and --system-default--. The chooser will basically show a menu so that the client can select the webmail at that point.

<%ifblock:isadmin%> 
To set the --system-default--, go to [b] admin home -> advanced -> general options [/b] and then you can set the default webmail system. --system-default-- is the webmail program that will shown if this particular value is left blank.
</%ifblock>



