
 There are two kinds of redirection. You can either redirect a local address to another local address or you can redirect a local address to remote address.

 To redirect [b] /source [/b] to another location, say [b] /destination [/b], click on [b] 'add local redirection' [/b], and then add '/source' as the [b] virtual location [/b] and '/destination' as the [b] redirected location. [/b]

 To redirect [b] /source [/b]  to [b] http://destination.com, [/b]  click on  [b]  'add remote redirection' [/b] , and then add '/source' as the virtual location, and destination.com as the redirected location.
