
 Dns template is the DNS configuration which will be copied to the domains DNS when a new domain is created. For adding a DNS template, you have to basically specify the primary and secondary nameservers for the domains you are planning to create on this server. For instance: [b] ns.lxlabs.com and ns1.lxlabs.com [/b]. DNS template name is simply an identifier which will be used later when creating the domain.



