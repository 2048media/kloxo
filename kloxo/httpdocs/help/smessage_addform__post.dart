
 Messages are a great means to communicate with all or a specific group of your clients. Please note that this is one way communication and the clients cannot reply back. If you want a conversation, you will need to use the ticketing system to talk to a single client. Kloxo will automatically notify the person when he logs in, if he has any unread messages or tickets left.

 You can enable on the [b] send mail [/b] checkbox so that a mail is also sent to all the clients you have selected. 
