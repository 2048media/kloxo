
Mysqldb allows you to create and manage mysql databases. Please note that the username for the a mysql database is the same as the mysql dbname, and also that the username is always prefixed at the beginning. So if you add a database 'called' mydb, the actual database name would be your-username_mydb, and  the username would be
your-username_mydb


 SO to access the database your-username__mydb, you have to use the same user your-username__mydb, and the password you have supplied here.
